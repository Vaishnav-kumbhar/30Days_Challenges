// Artithmetic Operations
console.log(2 + 2);
console.log(2 - 2);
console.log(2 * 2);
console.log(2 / 2);
console.log(2 % 2);

// Assignment Operators
let num = 4;
num += 2;
console.log(num);
num -= 2;
console.log(num);

// Comparison Operators
console.log(9 > 5, 10 < 2);
console.log(9 >= 9, 5 <= 8);
console.log("9" === 5, 10 == 2);

// Logical Operators
if (10 >= 10 && 5 === 5) console.log("all good");
if (10 >= 10 || 10 === 5) console.log(" good");
if (!10 >= 5) console.log("true");

// Ternary Operator
10 > 15 ? console.log("true") : console.log("true");
